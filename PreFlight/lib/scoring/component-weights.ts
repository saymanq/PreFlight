export function searchComponents(query: string): ComponentMeta[] {
  const q = query.toLowerCase();
  return ALL_COMPONENTS.filter(
    (c) =>
      c.name.toLowerCase().includes(q) ||
      c.description.toLowerCase().includes(q) ||
      c.bestFor.some((b) => b.toLowerCase().includes(q)) ||
      c.languages.some((l) => l.toLowerCase().includes(q)) ||
      c.usedBy.some((u) => u.toLowerCase().includes(q))
  );
}

export function getAlternatives(key: string): ComponentMeta[] {
  const comp = COMPONENT_MAP[key];
  if (!comp) return [];
  return ALL_COMPONENTS.filter(
    (c) => c.key !== key && c.subcategory === comp.subcategory
  );
}

